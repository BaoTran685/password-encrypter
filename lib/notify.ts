
import { toast } from 'react-toastify';

export const notify_error = (txt: String) => toast.error(txt, {
  position: "bottom-right",
  autoClose: 5000,
  hideProgressBar: false,
  closeOnClick: true,
  pauseOnHover: true,
  draggable: true,
  progress: undefined,
  theme: "light",
});
